CONSTRAINT chk_detalles_avaluos_precio
CHECK (precio_unitario >= 0)
);

-- MENU
CREATE TABLE menu (
id_menu        INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
nombre         VARCHAR(100) NOT NULL,
es_submenu     BOOLEAN DEFAULT FALSE,
url            VARCHAR(150),
id_menu_parent INT,
estado         BOOLEAN DEFAULT TRUE,
show           BOOLEAN DEFAULT TRUE,

CONSTRAINT fk_menu_parent
FOREIGN KEY (id_menu_parent)
REFERENCES menu (id_menu)
ON DELETE SET NULL
);

-- PERMISOS
CREATE TABLE permisos (
permiso_id          INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
usuario_id          INT NOT NULL,
id_menu             INT NOT NULL,
estado              BOOLEAN DEFAULT TRUE,
fecha_creacion      TIMESTAMPTZ DEFAULT NOW(),
fecha_actualizacion TIMESTAMPTZ,
fecha_eliminacion   TIMESTAMPTZ,

CONSTRAINT fk_perm_usuario
FOREIGN KEY (usuario_id)
REFERENCES usuarios (usuario_id)
ON DELETE CASCADE,

CONSTRAINT fk_perm_menu
FOREIGN KEY (id_menu)
REFERENCES menu (id_menu)
ON DELETE CASCADE,

CONSTRAINT permisos_unique
