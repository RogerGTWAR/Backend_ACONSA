);

-- COMPRAS
CREATE TABLE compras (
compra_id           INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
proveedor_id        INT NOT NULL,
empleado_id         INT,
numero_factura      VARCHAR(50) NOT NULL,
fecha_compra        DATE NOT NULL DEFAULT CURRENT_DATE,
monto_total         NUMERIC(18,2) NOT NULL DEFAULT 0,
estado              VARCHAR(50) NOT NULL DEFAULT 'Pendiente',
observaciones       VARCHAR(200),
fecha_creacion      TIMESTAMPTZ DEFAULT NOW(),
fecha_actualizacion TIMESTAMPTZ,
fecha_eliminacion   TIMESTAMPTZ,

CONSTRAINT fk_compras_proveedor
FOREIGN KEY (proveedor_id)
REFERENCES proveedores (proveedor_id),

CONSTRAINT fk_compras_empleado
FOREIGN KEY (empleado_id)
REFERENCES empleados (empleado_id),

CONSTRAINT uq_compras_numero_factura
UNIQUE (numero_factura),

CONSTRAINT chk_compras_monto
CHECK (monto_total >= 0),

CONSTRAINT chk_compras_estado
CHECK (estado IN ('Pendiente','Pagada','Cancelada'))
);

-- DETALLES COMPRAS
