empleado_id          INT NOT NULL,
vehiculo_id          INT NOT NULL,
fecha_asignacion     DATE NOT NULL,
fecha_fin_asignacion DATE,
descripcion          VARCHAR(100),
fecha_creacion       TIMESTAMPTZ DEFAULT NOW(),
fecha_actualizacion  TIMESTAMPTZ,
fecha_eliminacion    TIMESTAMPTZ,

CONSTRAINT fk_detalle_empleado_veh
FOREIGN KEY (empleado_id)
REFERENCES empleados (empleado_id),

CONSTRAINT fk_detalle_vehiculo
FOREIGN KEY (vehiculo_id)
REFERENCES vehiculos (vehiculo_id),

CONSTRAINT chk_detalles_vehiculos_fechas
CHECK (fecha_fin_asignacion IS NULL OR fecha_fin_asignacion >= fecha_asignacion)
);

-- USUARIOS
CREATE TABLE usuarios (
usuario_id          INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
